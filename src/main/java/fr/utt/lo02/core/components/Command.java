package fr.utt.lo02.core.components;

/**
 * The Command enum represents the different types of commands that can be issued in the game.
 */
public enum Command {
    /**
     * Command to expand the player's territory.
     */
    EXPAND,

    /**
     * Command to explore new areas.
     */
    EXPLORE,

    /**
     * Command to exterminate enemy units.
     */
    EXTERMINATE
}